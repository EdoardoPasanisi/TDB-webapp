export const DOG_BREEDS_BY_NAME = new Map(
  DOG_BREEDS.map((breed) => [normalizeDogBreedLookupKey(breed.name), breed] as const)
);

const DOG_BREEDS_BY_LOOKUP = new Map<string, DogBreed>();

for (const breed of DOG_BREEDS) {
  for (const term of [breed.name, ...breed.aliases]) {
    const normalizedTerm = normalizeDogBreedLookupKey(term);
    if (normalizedTerm && !DOG_BREEDS_BY_LOOKUP.has(normalizedTerm)) {
      DOG_BREEDS_BY_LOOKUP.set(normalizedTerm, breed);
    }
  }
}

export function findDogBreed(value: string | null | undefined): DogBreed | null {
  const normalized = normalizeDogBreedLookupKey(value);
  if (!normalized) return null;
  return DOG_BREEDS_BY_LOOKUP.get(normalized) ?? null;
}
